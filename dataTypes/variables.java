package dataTypes;

public class variables {
    public static void main(String[] args) {
        byte b = 23;
        int i = 3908;
        short s = 3920;
        float f = 943.54f;
        char c = 'c';
        String S = "ankur";
        System.out.println("byte variable value " + b);
        System.out.println("integer variable value " + i);
        System.out.println("short variable value " + s);
        System.out.println("float variable value " + f);
        System.out.println("character variable value " + c);
        System.out.println("String variable value " + S);
    }
}
